int	ft_isdigit(char c)
{
	if (c >= 48 && c <= 57)
		return (1);
	return (0);
}

int ft_string_is_num(char *str)
{
	while (*str)
	{
		if (!ft_isdigit(*str)) 
			return (0);
		str++;
	}
	return (1);
}